print("Programa que calcula la superficie de cualquier circunferencia")
#Paso 1. Solicitar el radio por medio del teclado
radio = float(input("Ingrese el radio de la circunferencia del circulo: "))
#Paso 2. Aplicar la formula
superficie = 3.1416 * (radio ** 2)
#paso 3. Mostrar el resultado
print("La superficie es: ", superficie)
