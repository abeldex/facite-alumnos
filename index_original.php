									} else if(data == 'contra')
									{
										swal('', "La contraseña es incorrecta! Intenta de nuevo", "warning");
									} else if(data == 'cuenta')
									{
										swal('', "El usuario no se encuentra registrado", "error");
									}
					   },
								 error: function( jqXhr, textStatus, errorThrown ){
